 # Prototyping
 
 The intent of this document is to provide tools and methodologies to quickly prototype using iOS.

 Prototyping on iOS is much cheaper than full-scale production. Prototypes are designed to answer just a few questions, so they are much cheaper and faster to develop than applications that go into production. We can build many different prototypes to test a specific aspect:
 
 - Architecture
 - New functionality in an existing system
 - Third-party tools or components
 - [User interface](User%20Interface)
 
 Prototype to learn.
 
